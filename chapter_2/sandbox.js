//switch statements

const grade = "D";

switch (grade) {
  case "A":
    console.log("you got an A");
    break;
  case "B":
    console.log("you got a B");
    break;
  case "C":
    console.log("you got a C");
    break;
  case "D":
    console.log("you got a D");
    break;
  case "E":
    console.log("you got a E");
    break;
  case "F":
    console.log("you got an F");
    break;
  default:
    console.log("not a valid grade");
}
